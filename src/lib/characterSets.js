const basic = Array.from({ length: 95 }, (_, i) => String.fromCodePoint(32 + i))

const extendedExtra = [
  'À','Á','Â','Ã','Ä','Å','Æ','Ç','È','É','Ê','Ë',
  'Ì','Í','Î','Ï','Ð','Ñ','Ò','Ó','Ô','Õ','Ö','Ø',
  'Ù','Ú','Û','Ü','Ý','Þ','ß',
  'à','á','â','ã','ä','å','æ','ç','è','é','ê','ë',
  'ì','í','î','ï','ð','ñ','ò','ó','ô','õ','ö','ø',
  'ù','ú','û','ü','ý','þ','ÿ',
  'Ā','ā','Ă','ă','Ą','ą','Ć','ć','Č','č','Ď','ď',
  'Đ','đ','Ě','ě','Ę','ę','Ğ','ğ','Í','Ĺ','ĺ','Ľ',
  'ľ','Ł','ł','Ń','ń','Ň','ň','Ő','ő','Œ','œ','Ř',
  'ř','Ś','ś','Š','š','Ş','ş','Ť','ť','Ţ','ţ','Ů',
  'ů','Ű','ű','Ź','ź','Ż','ż','Ž','ž',
]

const extended = [...basic, ...extendedExtra]

export const SETS = { basic, extended }

export function getCharacterSet(type, custom = []) {
  if (type === 'custom') return custom
  return SETS[type] ?? basic
}
